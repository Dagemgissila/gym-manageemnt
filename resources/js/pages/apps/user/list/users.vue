    key: 'actions',
    sortable: false,
  },
]


const users=ref();
onMounted(() => {
  console.log("Fetching users...");

  const apiUrl = createUrl('/users', {  
    query: {
      q: toValue(searchQuery),
      status: toValue(selectedStatus),
      role: toValue(selectedRole),
      itemsPerPage: toValue(itemsPerPage),
      page: toValue(page),
      sortBy: toValue(sortBy),
      orderBy: toValue(orderBy),
    },
  }).value; // Ensure we get the final URL string

  console.log("Final API URL:", apiUrl); // Debugging

  axiosAdmin.get(apiUrl) // Pass plain string URL
    .then((response) => {
      users.value=response.data;
      console.log("Users:", response);
    })
    .catch((error) => {
      console.error("Error fetching users:", error);
    });
});



// ðŸ‘‰ search filters
const roles = [
  {
    title: 'Admin',
    value: 'admin',
  },
  {
